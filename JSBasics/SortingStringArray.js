//sorting strings
let fruits = ["banana","mango","pomegranate","apple"]
//ascending order
console.log(fruits.sort())
//descending order
console.log(fruits.reverse())


//sorting numbers
var scores = [12,003,19,07,28,17]
//ascending order
console.log(scores.sort((a,b)=>a-b))
//descending order
console.log(scores.sort((a,b)=>b-a))